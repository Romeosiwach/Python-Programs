# Python has six compatison operators <,<=,>,>=,==,!= #

#1# Less than <
a=10
b=20
x=(a<b)
print(x)

#2# Less than equal to <=
a=10
b=10
z=(a<=b)
print(z)

#3# Greater than >
a=10
b=20
z=(a>b)
print(z)

#4# Greater than equal to >=
a=10
b=20
z=(a>=b)
print(z)

#5# Equal to ==
a=10
b=10
z=(a==b)
print(z)

#6# Not Equal to !=
a=10
b=20
z=(a!=b)
print(z)

