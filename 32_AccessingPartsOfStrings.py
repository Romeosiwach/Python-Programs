# Python Strings -> Python strings are the data types consist of english characters, and in python we can access a single character or group of characterd through stings.
string='ABCDEFGHIJKLMNOPQRSTUVWXYZ'

print(string) # ABCDEFGHIJKLMNOPQRSTUVWXYZ

print(string[2]) #Output: C

print(string[-2]) #Output: Y

print(string[0:2]) #Output: AB

print(string[2:8]) #Output: CDEFGH

print(string[25])  #Output: Z

print(string[0:0])  #Output: BLANK

print(string[:])   #Output: ABCDEFGHIJKLMNOPQRSTUVWXYZ

print(string[:2])  #Output: AB

print(string[2:])  #Output: CDEFGHIJKLMNOPQRSTUVWXYZ

print(string[:-12])  #Output: ABCDEFGHIJKLMN

print(string[-12:])  #Output: OPQRSTUVWXYZ

print(len(string))   #Output: 26


