# Type-Casting -> Type casting is the process of converting one type to another according to requirement. These are of 5 types
#1 int() ->
num=2.45678
print(int(num)) # Output: 2

mybool=True
print(int(mybool)) # Output: 1

myString="Python"
print(int(myString)) # Output: String can not be converted to int

myComplex=(2+3j) # Output: Complex can not be converted to int


#2 float() ->
num=2.45678
print(float(num)) # Output: 2.45678

mybool=True
print(float(mybool)) # Output: 1.0

myString="Python"
print(float(myString)) # Output: String can not be converted to int

myComplex=(2+3j) # Output: Complex can not be converted to int

# str() ->
num=2.45678
