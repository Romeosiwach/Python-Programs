# Python_Data_Types -> Data types in python consists of two terms mutable data types and immutable data types which are as follows.

# Mutable data types ->
# These are the data types which can be changed or modified according to use i.e [Lists, Sets, Dictionaries, All user defined classes].

num=[1,2,3,4,5,6]
print(num)
print(id(num))
num[0]=9
print(num)
print(id(num))


# Immutable data types ->
# These are the data types which can not be modified or edited furthe.  i.e [Numbers, Strings, Truples, Frozen-sets, All user defined classes]

